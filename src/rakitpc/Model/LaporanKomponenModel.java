package rakitpc.Model;

public class LaporanKomponenModel {

    private String kodeKomponen;
    private String namaKomponen;
    private String kategori;
    private String brand;
    private String socket;
    private String jenisMemori;
    private double harga;
    private int stokTersedia;
    private String jumlahTerpakai;
    private String noRakit;

    public String getKodeKomponen() {
        return kodeKomponen;
    }

    public void setKodeKomponen(String kodeKomponen) {
        this.kodeKomponen = kodeKomponen;
    }

    public String getNamaKomponen() {
        return namaKomponen;
    }

    public void setNamaKomponen(String namaKomponen) {
        this.namaKomponen = namaKomponen;
    }

    public String getKategori() {
        return kategori;
    }

    public void setKategori(String kategori) {
        this.kategori = kategori;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getSocket() {
        return socket;
    }

    public void setSocket(String socket) {
        this.socket = socket;
    }

    public String getJenisMemori() {
        return jenisMemori;
    }

    public void setJenisMemori(String jenisMemori) {
        this.jenisMemori = jenisMemori;
    }

    public double getHarga() {
        return harga;
    }

    public void setHarga(double harga) {
        this.harga = harga;
    }

    public int getStokTersedia() {
        return stokTersedia;
    }

    public void setStokTersedia(int stokTersedia) {
        this.stokTersedia = stokTersedia;
    }

    public String getJumlahTerpakai() {
        return jumlahTerpakai;
    }

    public void setJumlahTerpakai(String jumlahTerpakai) {
        this.jumlahTerpakai = jumlahTerpakai;
    }

    public String getNoRakit() {
        return noRakit;
    }

    public void setNoRakit(String noRakit) {
        this.noRakit = noRakit;
    }
}
